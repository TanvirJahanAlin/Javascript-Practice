function firstparameterization(dataOne, dataTwo) {
    const sum = dataOne + dataTwo;
    return sum;
}

function secondparameterization(dataOne, dataTwo, dataThree) {
    const sum = dataOne + dataTwo + dataThree;
    return sum;
}
console.log("Current Value is : " + firstparameterization(20,30));
console.log("Current Value is : " + secondparameterization(100,10,30));
