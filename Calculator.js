function calculator(num1, num2, num3, num4, num5) {
    const sum = num1 + num2;
    const subtract = sum - num3;
    const multiply = subtract * num4;
    const division = multiply / num5;
    return division;

}
console.log("the result is : " + calculator(100, 20, 30, 40, 2));



